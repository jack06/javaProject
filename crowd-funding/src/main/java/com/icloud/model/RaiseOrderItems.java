package com.icloud.model;

import java.util.Date;

public class RaiseOrderItems {
    private Long id;

    private Long orderId;//订单id

    private Long raiseId;//众筹id

    private Long luckyNo;//幸运号

    private Date createDate;//参与时间，用来确认幸运号

    private Long userId;//用户id

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getOrderId() {
		return orderId;
	}

	public void setOrderId(Long orderId) {
		this.orderId = orderId;
	}

	public Long getRaiseId() {
		return raiseId;
	}

	public void setRaiseId(Long raiseId) {
		this.raiseId = raiseId;
	}

	public Long getLuckyNo() {
		return luckyNo;
	}

	public void setLuckyNo(Long luckyNo) {
		this.luckyNo = luckyNo;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

    
}