package com.icloud.dto;

import java.util.Date;

public class RaiseOrderDetailDto {

	private Long orderId;		//订单id
	private Long userId;		//用户id
	private String nickName;	//用户昵称
	private Date createDate;	//参与时间
	private Long luckyNo;		//幸运号
	private String isAlert;		//是否提示：0未提示，1已提示
	
	public Long getOrderId() {
		return orderId;
	}
	public void setOrderId(Long orderId) {
		this.orderId = orderId;
	}
	public Long getUserId() {
		return userId;
	}
	public void setUserId(Long userId) {
		this.userId = userId;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	public Long getLuckyNo() {
		return luckyNo;
	}
	public void setLuckyNo(Long luckyNo) {
		this.luckyNo = luckyNo;
	}
	public String getIsAlert() {
		return isAlert;
	}
	public void setIsAlert(String isAlert) {
		this.isAlert = isAlert;
	}
}
