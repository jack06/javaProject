package com.icloud.dto;

public class WxFansRaiseInfoDto extends BaseDto{

	private Long userId;
	private String nickName;
	private String phone;
	private String deliveryAddress;
	private Long orderId;
	
	public WxFansRaiseInfoDto(){}
	
	public WxFansRaiseInfoDto(String resultType, String resultCode, String errorMsg){
		super(resultType, resultCode, errorMsg);
	}
	
	public Long getUserId() {
		return userId;
	}
	public void setUserId(Long userId) {
		this.userId = userId;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getDeliveryAddress() {
		return deliveryAddress;
	}
	public void setDeliveryAddress(String deliveryAddress) {
		this.deliveryAddress = deliveryAddress;
	}
	public Long getOrderId() {
		return orderId;
	}
	public void setOrderId(Long orderId) {
		this.orderId = orderId;
	}
	
}
